package model;

class Classmates {


    private String name;
    private String age;
    private String color;

    public String getName() {
        return this.name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getage() {
        return this.age;
    }
    public void setage(String age) {
        this.age = age;
    }
    public String getcolor() {
        return this.color;
    }
    public void setcolor(String color) {
        this.color = color;
    }
}





